import { cookies } from "next/headers";

export type PortalLang = "de" | "en";

export function getPortalLang(): PortalLang {
  return cookies().get("jn_lang")?.value === "en" ? "en" : "de";
}

export function getPortalDict(lang: PortalLang) {
  return PORTAL_DICT[lang];
}

